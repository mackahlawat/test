package Chapter4Operators;

public class IncrementOperator {

	public static void main(String[] args) {
		int a = 9;
		int b = 7;
		int c;
		int d;
		c = ++b;
		d = a++;
		c++;
	     System.out.println("a  =  " + a );
	     System.out.println("b  =  " + b );
	     System.out.println("c  =  " + c );
	     System.out.println("d  =  " + d );
		
				
		

	}

}
